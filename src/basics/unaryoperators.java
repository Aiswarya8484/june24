package basics;

public class unaryoperators {

	public static void main(String[] args) {
    int v=20;
    System.out.println(v++);
    System.out.println(v);
    System.out.println(++v);
    System.out.println(v--);
    System.out.println(v);
    System.out.println(--v);
    




    
    
	}

}
